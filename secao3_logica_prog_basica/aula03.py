"""
Python = Liguagem de programação
Tipo de tipagem = Dinâmica/forte    
str -> string -> texto
Strings são texto que estão dentro de aspas
"""

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"')
# Aspas duplas
print(2, "Luiz 'Otávio'")

# Escape
print("Luiz \"Otávio\"")

# r
print(r"Luiz \"Otávio\"")
