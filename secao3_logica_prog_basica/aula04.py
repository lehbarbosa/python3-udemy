# Tipos int e float
# int -> Número Inteiro 
# O tipo int representa qualque número
# positivo ou negativo. int sem sinal é considerado positivo.
print(11)
print(-11)
print(0)

# float -> Número com ponto flutuante
# O tipo float represent qualquer número positivo ou negativo 
# com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1.5)

# A função type mostra o tipo que o Python
# inferiu ao valor.
print(type('Otávio'))
print(type(0))
print(type(1.0), type(-1.1), type(0.0))


